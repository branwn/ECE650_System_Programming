package com.banmajio.bean;



public class Player {
	private int player_id;
	private int team_id;
	private int uniform_num;
	private String first_name;
	private String last_name;
	private int mpg;
	private int ppg;
	private int rpg;
	private int apg;
	private double spg;
	private double bpg;

	public int getPlayer_id() {
		return player_id;
	}

	public void setPlayer_id(int player_id) {
		this.player_id = player_id;
	}

	public int getTeam_id() {
		return team_id;
	}

	public void setTeam_id(int team_id) {
		this.team_id = team_id;
	}

	public int getUniform_num() {
		return uniform_num;
	}

	public void setUniform_num(int uniform_num) {
		this.uniform_num = uniform_num;
	}

	public String getFirst_name() {
		return first_name;
	}

	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}

	public String getLast_name() {
		return last_name;
	}

	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}

	public int getMpg() {
		return mpg;
	}

	public void setMpg(int mpg) {
		this.mpg = mpg;
	}

	public int getPpg() {
		return ppg;
	}

	public void setPpg(int ppg) {
		this.ppg = ppg;
	}

	public int getRpg() {
		return rpg;
	}

	public void setRpg(int rpg) {
		this.rpg = rpg;
	}

	public int getApg() {
		return apg;
	}

	public void setApg(int apg) {
		this.apg = apg;
	}

	public double getSpg() {
		return spg;
	}

	public void setSpg(double spg) {
		this.spg = spg;
	}

	public double getBpg() {
		return bpg;
	}

	public void setBpg(double bpg) {
		this.bpg = bpg;
	}
}
